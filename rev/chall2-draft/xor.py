# XOR key
key = 0x5A

# Encrypted values (ordered from local_d8 to local_b1)
encrypted_bytes = [
    0x09, 0x3f, 0x39, 0x2f, 0x28, 0x33, 0x34, 0x3f, 0x2e, 0x29,
    0x1f, 0x14, 0x13, 0x0e, 0x21, 0x22, 0x6a, 0x28, 0x05, 0x23,
    0x6a, 0x2f, 0x28, 0x05, 0x2d, 0x6e, 0x23, 0x05, 0x33, 0x34,
    0x05, 0x69, 0x6b, 0x6b, 0x68, 0x6b, 0x63, 0x6c, 0x62, 0x27
]

# Decrypt each byte with XOR
decrypted = [chr(byte ^ key) for byte in encrypted_bytes]

# Join characters to form the flag
flag = ''.join(decrypted)

print("Decrypted flag:", flag)

